# ヒストグラム(1000個、25分割)
x = rnorm(1000)
par(family="HiraMaruProN-W4")                  
hist(x,25,xlab=' 正規乱数値',ylab=' 度数',main=' 正規分布(n=1000)')
# ヒストグラム(1000個、30分割)
x = rnorm(1000)
par(family="HiraMaruProN-W4")                  
hist(x,30,xlab=' 正規乱数値',ylab=' 度数',main=' 正規分布(n=1000)')
# ヒストグラムと近似曲線(1000個、30分割)
x = rnorm(1000)
par(family="HiraMaruProN-W4")                  
hist(x,30,prob=TRUE,xlab=' 正規乱数値',ylab=' 度数',main=' ヒストグラムと近似曲線')
lines(density(x))

